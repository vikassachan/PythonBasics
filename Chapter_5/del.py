fruits =['kiwi1','orange2','apple3','grapes4']

print(fruits)

##POP mrthod

fruits.pop(1) ## Deletes specified value
print(fruits)

fruits.pop()  ## Deletes last value
print(fruits)


##Delete Operator
del fruits[1]
print(fruits)


